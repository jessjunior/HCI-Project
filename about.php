<p style="text-align: justify;">Greign Estates Limited is a professional Valuation and Real Estate Consultancy Company operating in accordance with the Valuer’s Act Chapter 532 and Estate Agents Act Chapter 533 of the Laws of Kenya. The Company was established in 2005 and incorporated in early 2010. It is well represented in the country with it’s Headquarters in Nairobi and a branch in Nyeri, Mombasa and Kiambu. The leading consultants in the company are registered with the bodies regulating the activities of Valuers and Estate Agency Registration Boards which are the statutory bodies regulating the activities of Valuers and Estate Agents in the country as per the requirements of the Valuers and Estate Agents Acts of the Laws of Kenya. They are also Full Members of the Institution of Surveyors of Kenya (I.S.K).</p>
<p style="text-align: justify;">This Institution, mainly through the Valuation and Estate Management Surveyors (V.E.M.S.) Chapter, governs our professional activities. Indeed the valuation standards in this country are outlined in the Valuers Handbook (Pages 118-132) which is based on the Internationally accepted standards as stipulated by the International Valution Standards Committee (I.V.S.C.), which we all subscribe to.</p>
